def area(a, h):
    '''Принимает числа a, h, возвращает произведение a и h деленное на 2 (площадь треугольника высотой h и основанием a)'''
    return a * h / 2 

def perimeter(a, b, c): 
    '''Принимает числа a, b, c, возвращает произведение сумму данных чисел (периметр треугольника со стронами a,b,c)'''
    return a + b + c 
